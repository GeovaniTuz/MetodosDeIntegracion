package integracion;

public class csTrapecio {

    //lo que se necesita en el trapecio
    private double a;
    private double b;
    private double h;
    private int n;

    // Alt + insert get and set
    public double getA() {
        return a;
    }

    public void setA(double a) {
        this.a = a;
    }

    public double getB() {
        return b;
    }

    public void setB(double b) {
        this.b = b;
    }

    public double getH() {
        return h;
    }

    public void setH(double h) {
        this.h = h;
    }

    public int getN() {
        return n;
    }

    public void setN(int n) {
        this.n = n;
    }

}
